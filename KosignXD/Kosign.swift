import Foundation

public class Kosign {
    
    private init() {}
    
    public class func add(v1: Int, v2: Int) -> Int {
        return v1 + v2
    }
    
    public class func sub(v1: Int, v2: Int) -> Int {
        return v1 - v2
    }
    
}
